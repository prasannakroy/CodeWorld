template<typename t>
using vc = vector<t>;

auto convert(int n) {
    vc<int> a;
    while (n) a.push_back(n % 10), n /= 10;
    reverse(span(a));
    return a;
}

int adjust(int n) {
    bool flag = 0;
    while (!flag) {
        flag = 1;
        auto a = convert(n);
        for (auto &i: a) if (i and n % i) {
            flag = 0;
            break;
        }
        if (!flag) n++;
    }
    return n;
}

