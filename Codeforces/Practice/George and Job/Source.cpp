template<typename t>
using vc = vector<t>;

void solve() {
    int n, m, k;
    cin >> n >> m >> k;
    vc<int> a(n + 1, 0);
    for (int i = 1; i <= n; i++) cin >> a[i], a[i] += a[i - 1];
    vc<vc<int>> dp(n + 1, vc<int>(k + 1, LLONG_MIN));
    for (int i = 0; i <= n; i++) dp[i][0] = 0;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= k; j++) {
            dp[i][j] = dp[i - 1][j];
            if (i - m + 1 > 0) dp[i][j] = max(dp[i][j], dp[i - m][j - 1] + a[i] - a[i - m]);
        }
    }
    cout << dp[n][k] << "\n";
}

