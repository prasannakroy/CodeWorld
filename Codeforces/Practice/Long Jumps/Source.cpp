void solve() {
	int n;
	cin >> n;
	vc<int> a(n + 1), dp(n + 1, 0);
	for (int i = 1; i <= n; i++) cin >> a[i];
	dp[n] = a[n];
	for (int i = n; i > 0; i--) {
		if (i + a[i] > n) dp[i] = a[i];
		else dp[i] = a[i] + dp[i + a[i]];
	}
	int mx(0);
	for (auto &i: dp) 
		mx = max(mx, i);
	cout << mx << "\n";
}

