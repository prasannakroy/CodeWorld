int compute(int x, int y) {
    if (x >= y) return (x - y);
    if (m[y]) return m[y];

    int res = y - x;
    if (y & 1) res = min({res, compute(x, y + 1) + 1, compute(x, y - 1) + 1});
    else res = min(res, compute(x, y / 2) + 1);
    return m[y] = res;
}

